# English: 1. Significance level. This is usually left at the standard value of 0.05 (which is related to the choice of a 95% confidence interval)
# 2. Treatment effect size. What effect size do you *expect*? This is a guess! But it can be an educated guess based on past studies. Often it's useful to think conservatively (i.e., smaller magnitude that what from past studies or what you hope for).
# 3. Standard deviation of the outcome variable. Think about the control group and ask what is the standard deviation of your outcome? This is a measure of the variability of the outcome. A normally-distributed outcome with a mean of zero a standard deviation of 5 will often have values as large as -10 and 10. But one with a standard deviation of 1 will typically have values between only -4 and 4. What will it be? This also a guess. This matters because the power is related to both the treatment effect size and the standard deviation. If the standard deviation is small, it is easier to detect any treatment effect all else equal. If it's larger, you might only be able to detect a large effect size.
# 4. Power target. This is like the significance level, it's typically fixed, so you can leave it at 80%, which means that you want to have an 80% probability of detecting a true effect.
# 5. Maximum number of subjects. This is just defining how wide the x-axis on the graph is.

# Français: 1. Niveau de signification. Ceci prend généralement la valeur standard de 0,05 (qui est liée au choix d'un intervalle de confiance à 95%).
# 2. Taille de l'effet du traitement. Quelle taille d'effet *attendez-vous*? C'est une supposition. Mais cela peut être une supposition éclairée basée sur des études précédentes. Il est souvent utile de penser de manière conservatrice (c'est-à-dire une taille d'effet plus petite que celle des études précédentes ou de ce que vous espérez).
# 3. Écart type de la variable de résultat. Pensez au groupe de contrôle et demandez vous quel est l'écart type de votre résultat? Il s'agit d'une mesure de la variabilité du résultat. Un résultat qui suit une loi normale avec une moyenne de zéro et un écart type de 5 aura souvent des valeurs aussi grandes que -10 et 10. Mais un résultat avec un écart type de 1 aura généralement des valeurs entre -4 et 4. Ceci est aussi une supposition. Cela est important car la puissance est liée à la taille de l'effet du traitement et à l'écart type. Si l'écart type est faible, il est plus facile de détecter un effet de traitement toutes choses égales par ailleurs. S'il est plus grand, vous ne pourrez peut-être détecter qu'une grande taille d'effet.
# 4. Cible de puissance. C'est comme le niveau de signification, elle est généralement fixe, vous pouvez donc la laisser à 80 %, ce qui signifie que vous voulez avoir une probabilité de 80 % de détecter un véritable effet.
# 5. Nombre maximum de sujets. Il s'agit simplement de définir la largeur de l'axe des x sur le graphique.

# Español: 1. Nivel de significancia. Esto suele dejarse en el valor estándar de 0.05 (que está relacionado con la elección de un intervalo de confianza del 95%).
# 2. Tamaño del efecto del tratamiento. ¿Qué tamaño de efecto esperas? ¡Esto es una suposición! Pero puede ser una suposición educada basada en estudios anteriores. A menudo es útil pensar de manera conservadora (es decir, un tamaño más pequeño que lo que indican estudios previos o lo que esperas).
# 3. Desviación estándar de la variable de resultado. Piensa en el grupo de control y pregúntate cuál es la desviación estándar de tu resultado. Esto es una medida de la variabilidad del resultado. Un resultado distribuido normalmente con una media de cero y una desviación estándar de 5 a menudo tendrá valores tan grandes como -10 y 10. Pero uno con una desviación estándar de 1 típicamente tendrá valores solo entre -4 y 4. ¿Cuál será? Esto también es una suposición. Esto es importante porque el poder estadístico está relacionado tanto con el tamaño del efecto del tratamiento como con la desviación estándar. Si la desviación estándar es pequeña, es más fácil detectar cualquier efecto del tratamiento, manteniendo todo lo demás igual. Si es más grande, es posible que solo puedas detectar un tamaño de efecto grande.
# 4. Objetivo de poder estadístico. Esto es similar al nivel de significancia, generalmente se fija en 80%, lo que significa que deseas tener una probabilidad del 80% de detectar un efecto verdadero.
# 5. Número máximo de sujetos. Esto simplemente define qué tan amplio será el eje x en el gráfico.

# Português: 1. Nível de significância. Isso geralmente é mantido no valor padrão de 0,05 (o que está relacionado à escolha de um intervalo de confiança de 95%).
# 2. Tamanho do efeito do tratamento. Que tamanho de efeito você *espera*? Isso é um palpite! Mas pode ser um palpite fundamentado em estudos anteriores. Muitas vezes, é útil pensar de forma conservadora (ou seja, um tamanho menor do que o que indicam estudos anteriores ou o que você espera).
# 3. Desvio padrão da variável de resultado. Pense no grupo de controle e pergunte-se qual é o desvio padrão do resultado? Isso é uma medida da variabilidade do resultado. Um resultado distribuído normalmente com uma média de zero e um desvio padrão de 5 frequentemente terá valores tão grandes quanto -10 e 10. Mas um com um desvio padrão de 1 normalmente terá valores apenas entre -4 e 4. O que será? Isso também é um palpite. Isso é importante porque o poder estatístico está relacionado tanto com o tamanho do efeito do tratamento quanto com o desvio padrão. Se o desvio padrão for pequeno, é mais fácil detectar qualquer efeito do tratamento, mantendo tudo o mais igual. Se for maior, você pode detectar apenas um tamanho de efeito grande.
# 4. Objetivo de poder estatístico. Isso é semelhante ao nível de significância, geralmente fixado em 80%, o que significa que você deseja ter uma probabilidade de 80% de detectar um efeito verdadeiro.
